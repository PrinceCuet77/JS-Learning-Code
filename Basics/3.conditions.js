// if-else condition
const val = 5
if (val == 1) {
    console.log(val)
} else if (val == 2) {
    console.log(val)
} else {
    console.log(val)
}

// Output:
// 5

// switch statement
// 'break' must be present if I want to execute only one case. Otherwise skip 'break' for multiple next execution.
const dice = 5
switch (dice) {
    case 1:
        console.log(dice)
        break
    case 2:
        console.log(dice)
        break
    case 3:
        console.log(dice)
        break
    default:
        console.log(dice)
}

// Output:
// 5
